    int right = *max_element(A.begin(), A.end());
    while(left<right-1){
        ll mid = (left+right)/2;
        if (judge(mid, a, b, A)) {
            left = mid;
        }
        else {
            right = mid;
        }
    }

    if (judge(right, a, b, A)) cout << right << endl;
