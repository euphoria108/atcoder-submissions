bool judge(int c, int a, int b, vi A) {
    ll x = 0;  // if int, overflow occurs
    ll y = 0;
    for (auto a_ele : A) {
        if (a_ele>c) y += (a_ele-c)/b;
        else x += (c-a_ele+a-1)/a;  // ceil
    }
    // printf("c: %d, x: %d, y: %d\n", c, x, y);
    return x <= y;
}

